      <h3>Epics en Stories</h3>
      <p><em>Project management</em></p>
      <p>
        Epics zijn een handige manier om werk te organiseren en een hiërarchie
        te creëren. Het idee is om werk op te splitsen in opleverbare stukken,
        zodat grote projecten kunnen worden afgerond en klanten op regelmatige
        basis waarde krijgen. Epics helpen teams om overzicht te behouden en
        werk in beheersbare stukken te verdelen.
      </p>
      <p>
        Epics kunnen worden onderverdeeld in stories. User stories zijn
        gerichter en beschrijven specifieke functionaliteiten of
        gebruikersbehoeften.
      </p>
      <p>Structuur: <strong>Epic → Stories → User Stories → Taken</strong></p>

      <h3>MoSCoW Prioriteringsmodel</h3>
      <p>
        Het MoSCoW-model wordt gebruikt om prioriteiten te stellen tijdens
        projecten:
      </p>
      <ul>
        <li>
          <strong>Must have:</strong> Dit moet af zijn op de deadline. Als het niet
          af is, is het project gefaald.
        </li>
        <li>
          <strong>Should have:</strong> Dit zou af moeten zijn. Het project zou incompleet
          zijn zonder deze items.
        </li>
        <li>
          <strong>Could have:</strong> Dit kan worden toegevoegd als er tijd over
          is.
        </li>
        <li>
          <strong>Won't have:</strong> Goede ideeën die nuttig zijn, maar momenteel
          geen prioriteit hebben.
        </li>
      </ul>

