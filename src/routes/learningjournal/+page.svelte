      <h3>SvelteKit Structuur</h3>
      <p>De structuur van SvelteKit is als volgt:</p>
      <ul>
        <li>
          <strong>src</strong>: Dit bevat alles wat SvelteKit moet uitvoeren,
          zoals pages en components.
        </li>
        <li>
          <strong>static</strong>: Vergelijkbaar met de public-map in Node.js,
          bevat dit bestanden zoals favicon, afbeeldingen, en CSS-variabelen.
          Downloads horen hier niet in, tenzij het een SVG is.
        </li>
        <li>
          <strong>app.html</strong>: Dit is een belangrijk bestand, het vormt
          het begin van de applicatie.
        </li>
        <li>
          <strong>routes</strong>: Dit bestand bevat je pagina's en is cruciaal
          voor de navigatie binnen de applicatie.
        </li>
        <li>
          <strong>lib</strong>: Hier staat de herbruikbare code die je kunt
          importeren vanuit de library.
        </li>
        <li>
          <strong>server</strong>: Dit bevat alleen server-side code, zoals een
          inlogsysteem en API-sleutels. Deze mogen niet in page.js, alleen in
          page.server.js.
        </li>
        <li>
          <strong>gitignore</strong>: Bestanden zoals .env (voor API-sleutels en
          wachtwoorden) en node.modules mogen niet naar Git. Het
          .env.example-bestand is handig zodat mensen begrijpen dat er een
          .env-bestand nodig is.
        </li>
        <li>
          <strong>package.json</strong>: Dit bestand bevat alles wat nodig is om
          het project uit te voeren (bijvoorbeeld de Svelte-library).
        </li>
        <li>
          <strong>svelte.config</strong>: Hierin staat de configuratie van
          SvelteKit.
        </li>
        <li>
          <strong>vite.config.js</strong>: Dit is een tool om het project uit te
          voeren; het fungeert als een builder.
        </li>
      </ul>

